    <h3>Introduction</h3>

<p>The Student Recours Management System is a web-based platform developed to facilitate the management of student recours requests within the university. The system aims to streamline the process of handling recours requests, providing a user-friendly interface for both students and technical staff.</p>

    <h3>Key Features and Functionalities:</h3>

    <ol>
        <li><strong>Student Registration and Login:</strong>  <br> <strong> Description:</strong> Students can register on the platform by providing necessary information such as name, email, and group. After registration, they can log in using their credentials.
<br> <strong>  Implementation:</strong>  PHP, MySQL, HTML, and Bootstrap were used to create a secure and responsive registration and login system.</li>

     <br>   <li><strong>Student Recours Submission:</strong>  <br>  <strong> Description:</strong> Students can submit recours requests by filling out a form with details such as module, nature, note affiche, and note reel.
 <br> <strong>  Implementation:</strong> PHP and MySQL were used to store recours requests in a database. The submission form includes validation to ensure accurate data entry.</li>

     <br>   <li><strong>Student Recours Status Display:</strong>   <br> <strong> Description: </strong> Students can view the status of their recours requests, whether they are favorable, defavorable, or pending.
 <br> <strong>  Implementation: </strong>The system retrieves and displays recours status from the database. Bootstrap is used for styling.</li>

    <br>    <li><strong>Tech Dashboard:</strong> <br> <strong>Description:</strong>  Technical staff (tech) can view a list of recours requests with statuses other than 1 and 2. They can update the status to favorable or defavorable.
<br> <strong> Implementation: </strong>PHP, MySQL, and Bootstrap were used to create a dashboard for techs. Different colors represent recours statuse</li>

      <br>  <li><strong>About Us Page:</strong> <br>  <strong>     Description:</strong> An "About Us" page provides information about the purpose of the website and introduces the development team.
 <br> <strong>  Implementation:</strong>  HTML, PHP, and Bootstrap were used to create an informative page about the website and its creators.</li>

    <br>    <li><strong>Search Student Page:</strong> <br>    <strong>  Description: </strong> A search page allows users to find students based on email, first name, or last name. The results include student information and recours details if applicable.
  <br><strong> Implementation: </strong>  PHP, MySQL, and Bootstrap were used to create a search page that retrieves and displays student information.</li>
    </ol>

    <h3>Conclusion</h3>
     <p>The Student Recours Management System successfully addresses the needs of both students and technical staff by providing a centralized and efficient platform for managing recours requests. The project utilizes a combination of PHP, MySQL, HTML, and Bootstrap to create a responsive and user-friendly interface.</p>



